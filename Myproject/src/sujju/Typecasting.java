package sujju;

public class Typecasting {

	public static void main(String[] args) {
		
		byte b=120; 
		int  i=b;     
		System.out.println(i);
		
		float f=15.14f;
		double d=f;
		System.out.println(d);
		
		 
		
		double f1=10.52d;
		long l= (long)f1;
		System.out.println(l);
		
		float f2=34.56f;
		int li=(int) f2;
		System.out.println(li);

	}

}
