package sujju;

public class AssigmentOperators {

	public static void main(String[] args) {
		int a=15;
		
		//add and assign
		a+=5;
		System.out.println("a after +=" +a);
		
		a-=4;
		System.out.println("a after -=" +a);
		
		a*=6;
		System.out.println("a after *=" +a);
		
		a/=2;
		System.out.println("a after /=" +a);
		
		a%=3;
		System.out.println("a after %=" +a);
	}

}
	
		
		
				
		

		
		
		
		

	


