package sujju;

public class Logicaloperators {

	public static void main(String[] args) {
		int a=15, b=7, c=18;
		
		//logicl And
		System.out.println("(a>b)&&(b<c) :" +((a>b)&&(b<c)));
		
		//logical Or
		System.out.println("(a>b)||(b<c) :" +((a>b)||(b<c)));
		
		//logical Not
		System.out.println("!(a>b) :" +!(a>b));
		
	}
}	
		
		
		
		
		
	


